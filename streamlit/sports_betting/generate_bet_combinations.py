def parse_date(value: str) -> pd.Timestamp:
    parsed = pd.to_datetime(value, errors="coerce")
    if pd.isna(parsed):
        raise ValueError(f"Invalid date: {value!r}. Use YYYY-MM-DD format.")
    return pd.Timestamp(parsed.date())


def parse_combo_sizes(value: str) -> list[int]:
    sizes: list[int] = []
    for part in value.split(","):
        stripped = part.strip()
        if not stripped:
            continue
        size = int(stripped)
        if size < 2:
            raise ValueError("Combination sizes must be >= 2.")
        sizes.append(size)
    if not sizes:
        raise ValueError("At least one combo size is required.")
    return sorted(set(sizes))


def _read_optional_csv(path: Path, required_cols: list[str]) -> pd.DataFrame:
    if not path.exists():
        return pd.DataFrame()
    try:
        df = pd.read_csv(path)
    except Exception:
        return pd.DataFrame()
    if any(col not in df.columns for col in required_cols):
        return pd.DataFrame()
    for col in ("team", "team_name", "club", "home_team", "away_team"):
        if col in df.columns:
            df[col] = df[col].map(canonical_team_name)
    return df


def load_data(path: Path) -> pd.DataFrame:
    df = pd.read_csv(path)
    if "match_date" not in df.columns:
        raise ValueError("CSV missing required column: match_date")
    df["match_date"] = pd.to_datetime(df["match_date"], errors="coerce")
    df = df.loc[df["match_date"].notna()].copy()
    if "home_team" in df.columns:
        df["home_team"] = df["home_team"].map(canonical_team_name)
    if "away_team" in df.columns:
        df["away_team"] = df["away_team"].map(canonical_team_name)

    for col in STAT_COLUMNS:
        if col not in df.columns:
            df[col] = np.nan
        df[col] = pd.to_numeric(df[col], errors="coerce")
    return df


def league_priors(historical: pd.DataFrame) -> dict[str, LeaguePrior]:
    priors: dict[str, LeaguePrior] = {}
    grouped = historical.groupby("league_code", dropna=False)
    for league_code, chunk in grouped:
        total = max(len(chunk), 1)
        home_rate = float((chunk["result_ft"] == "H").sum()) / total
        draw_rate = float((chunk["result_ft"] == "D").sum()) / total
        away_rate = float((chunk["result_ft"] == "A").sum()) / total
        priors[str(league_code)] = LeaguePrior(home_rate, draw_rate, away_rate)
    if not priors:
        priors["global"] = LeaguePrior(0.44, 0.25, 0.31)
    return priors


def _prepare_team_match_rows(historical: pd.DataFrame) -> pd.DataFrame:
    base = historical.copy()
    for col in STAT_COLUMNS:
        base[col] = pd.to_numeric(base[col], errors="coerce").fillna(0.0)

    home = pd.DataFrame(
        {
            "match_date": base["match_date"],
            "league_code": base["league_code"],
            "team": base["home_team"],
            "is_home": 1,
            "points": np.where(base["result_ft"] == "H", 3, np.where(base["result_ft"] == "D", 1, 0)),
            "draw": (base["result_ft"] == "D").astype(int),
            "goals_for": base["home_goals_ft"],
            "goals_against": base["away_goals_ft"],
            "shots_for": base["home_shots"],
            "shots_against": base["away_shots"],
            "sot_for": base["home_shots_on_target"],
            "sot_against": base["away_shots_on_target"],
            "fouls": base["home_fouls"],
            "corners_for": base["home_corners"],
            "corners_against": base["away_corners"],
            "yellow": base["home_yellow_cards"],
            "red": base["home_red_cards"],
        }
    )

    away = pd.DataFrame(
        {
            "match_date": base["match_date"],
            "league_code": base["league_code"],
            "team": base["away_team"],
            "is_home": 0,
            "points": np.where(base["result_ft"] == "A", 3, np.where(base["result_ft"] == "D", 1, 0)),
            "draw": (base["result_ft"] == "D").astype(int),
            "goals_for": base["away_goals_ft"],
            "goals_against": base["home_goals_ft"],
            "shots_for": base["away_shots"],
            "shots_against": base["home_shots"],
            "sot_for": base["away_shots_on_target"],
            "sot_against": base["home_shots_on_target"],
            "fouls": base["away_fouls"],
            "corners_for": base["away_corners"],
            "corners_against": base["home_corners"],
            "yellow": base["away_yellow_cards"],
            "red": base["away_red_cards"],
        }
    )

    team_rows = pd.concat([home, away], ignore_index=True)
    team_rows["goal_diff"] = team_rows["goals_for"] - team_rows["goals_against"]
    team_rows["corners_diff"] = team_rows["corners_for"] - team_rows["corners_against"]
    team_rows["cards_weighted"] = team_rows["yellow"] + 2.0 * team_rows["red"]
    team_rows = team_rows.sort_values(["league_code", "team", "match_date"]).reset_index(drop=True)
    return team_rows


def _momentum_slope_from_series(points_series: "pd.Series") -> float:  # noqa: F821
    """OLS slope of a points sequence normalised by max-points-per-game (3).

    Positive = improving form; negative = declining form.
    Returns 0.0 if fewer than 2 observations.
    """
    import numpy as _np  # local import to keep module-level namespace clean
    vals = list(points_series)
    n = len(vals)
    if n < 2:
        return 0.0
    x = _np.arange(n, dtype=float)
    y = _np.array(vals, dtype=float)
    x_mean = x.mean()
    y_mean = y.mean()
    denom = float(_np.sum((x - x_mean) ** 2))
    if denom <= 0.0:
        return 0.0
    slope = float(_np.sum((x - x_mean) * (y - y_mean))) / denom
    return slope / 3.0  # normalise: WWWWW ≈ +0.9, LLLLL ≈ -0.9


def _last_n_features(team_rows: pd.DataFrame, window: int) -> pd.DataFrame:
    recs: list[dict[str, float | str]] = []
    for (league_code, team), g in team_rows.groupby(["league_code", "team"], dropna=False):
        tail = g.tail(window)
        if tail.empty:
            continue
        # Shots-on-target differential: sot_for - sot_against (proxy for xG)
        sot_diff_pg = float((tail["sot_for"] - tail["sot_against"]).mean()) if "sot_for" in tail.columns else 0.0
        recs.append(
            {
                "league_code": league_code,
                "team": team,
                "last_points_pg": float(tail["points"].mean()),
                "last_goal_diff_pg": float(tail["goal_diff"].mean()),
                "last_goals_for_pg": float(tail["goals_for"].mean()),
                "last_goals_against_pg": float(tail["goals_against"].mean()),
                "last_corners_diff_pg": float(tail["corners_diff"].mean()),
                "last_cards_pg": float(tail["cards_weighted"].mean()),
                "last_draw_rate": float(tail["draw"].mean()),
                # ── NEW v2 features ──────────────────────────────────────────
                "last_sot_diff_pg": sot_diff_pg,
                "last_momentum_slope": _momentum_slope_from_series(tail["points"]),
            }
        )
    return pd.DataFrame(recs)


def _team_aggregates(team_rows: pd.DataFrame, as_of_date: pd.Timestamp) -> pd.DataFrame:
    overall = (
        team_rows.groupby(["league_code", "team"], dropna=False)
        .agg(
            matches=("points", "size"),
            points=("points", "sum"),
            goals_for=("goals_for", "sum"),
            goals_against=("goals_against", "sum"),
            goal_diff=("goal_diff", "sum"),
            draw_rate=("draw", "mean"),
            cards_pg=("cards_weighted", "mean"),
            corners_diff_pg=("corners_diff", "mean"),
            sot_diff_pg=("sot_for", lambda x: float(x.mean()) if len(x) else 0.0),
            sot_against_pg=("sot_against", "mean"),
            last_match_date=("match_date", "max"),
        )
        .reset_index()
    )

    home_ppg = (
        team_rows.loc[team_rows["is_home"] == 1]
        .groupby(["league_code", "team"], dropna=False)["points"]
        .mean()
        .rename("home_ppg")
        .reset_index()
    )
    away_ppg = (
        team_rows.loc[team_rows["is_home"] == 0]
        .groupby(["league_code", "team"], dropna=False)["points"]
        .mean()
        .rename("away_ppg")
        .reset_index()
    )

    seven_days_ago = as_of_date - pd.Timedelta(days=7)
    matches_last7 = (
        team_rows.loc[team_rows["match_date"] >= seven_days_ago]
        .groupby(["league_code", "team"], dropna=False)
        .size()
        .rename("league_matches_last7")
        .reset_index()
    )

    agg = overall.merge(home_ppg, on=["league_code", "team"], how="left")
    agg = agg.merge(away_ppg, on=["league_code", "team"], how="left")
    agg = agg.merge(matches_last7, on=["league_code", "team"], how="left")

    agg["home_ppg"] = agg["home_ppg"].fillna(0.0)
    agg["away_ppg"] = agg["away_ppg"].fillna(0.0)
    agg["league_matches_last7"] = agg["league_matches_last7"].fillna(0.0)
    agg["goals_for_pg"] = agg["goals_for"] / agg["matches"].replace(0, np.nan)
    agg["goals_against_pg"] = agg["goals_against"] / agg["matches"].replace(0, np.nan)
    agg["goal_diff_pg"] = agg["goal_diff"] / agg["matches"].replace(0, np.nan)
    agg["goals_for_pg"] = agg["goals_for_pg"].fillna(0.0)
    agg["goals_against_pg"] = agg["goals_against_pg"].fillna(0.0)
    agg["goal_diff_pg"] = agg["goal_diff_pg"].fillna(0.0)
    agg["days_rest"] = (as_of_date - pd.to_datetime(agg["last_match_date"])).dt.days
    agg["days_rest"] = agg["days_rest"].fillna(14).clip(lower=0)
    return agg


def _league_positions(team_snapshot: pd.DataFrame) -> pd.DataFrame:
    ranked = team_snapshot.copy()
    ranked["position"] = (
        ranked.sort_values(["league_code", "points", "goal_diff", "goals_for"], ascending=[True, False, False, False])
        .groupby("league_code")
        .cumcount()
        + 1
    )
    return ranked


def _extract_importance_col(df: pd.DataFrame) -> str | None:
    for col in ("importance", "impact", "importance_score", "player_importance", "weight"):
        if col in df.columns:
            return col
    return None


